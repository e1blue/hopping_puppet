// プラグインクラス
class IosPluginSecret: NSObject{
	// ----------------------------------------------------------------

	static internal func iosPluginSecretGet(_ key: String, id: Int32) -> String{
		let cstr: [CChar] = key.cString(using: String.Encoding.utf8)!;
		let value: String? = String(cString: UnsafePointer<CChar>(gamePluginSecretGet(cstr, id)));
		return value!;
	}

	// ----------------------------------------------------------------
}

